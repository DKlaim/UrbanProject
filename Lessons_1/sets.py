set_0 = {1, 2, 3, 1, 2, 3, 4, 5, 'string', (1, 2, 3), True}
print(set_0) # множество/набор хранит только уникальные элементы в алфавитном порядке, повторяющиеся элементы отбрасывает
                # из булевого типа данных отображает только False, к элементам множества нельзя обратиться по индексу

