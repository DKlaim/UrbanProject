name = 'Denis'
print(name)

print("Hello, " + name + '!') # сложение строк

print('Ho' * 5) # умножение строк

print(name[0]) # вывод элемента строки по индексу

print(name[0:3]) # срез с первого элемента до 3-го (элемент под индексом 3 не включается в срез)

print(name[::2]) # срез с начала до конца строки, с шагом в 2 элемента

print(name[::-1]) # срез с конца до начала строки, с шагом в -1 элемент

