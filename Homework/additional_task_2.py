# Задача 1
# Дан список чисел. Определите, сколько в нем встречается различных чисел.
numbers = [1, 2, 3, 4, 5, 6, 12, 6, 3, 1]
print('########################')
print('Список:', numbers)
print('Количество уникальных чисел в списке:', len(set(numbers)))
