# Задача 2
# Даны два списка чисел. Посчитайте, сколько чисел содержится одновременно как в первом списке, так и во втором.
numbers_1 = [1, 2, 3, 4, 5, 6, 12, 6, 3, 1]
numbers_2 = [1, 2, 6, 12, 6, 3, 1]
print('########################')
print('Список №1:', numbers_1, '\nСписок №2:', numbers_2)
print('Количество одинаковых чисел в двух списках:', len(set(numbers_1) & set(numbers_2)))
print('########################\n')

